		/// The lowest priority.
		/// </summary>
		Lowest = 0,

		/// <summary>
		/// The low priority.
		/// </summary>
		Low = 1,

		/// <summary>
		/// The medium priority.
		/// </summary>
		Medium = 2,

		/// <summary>
		/// The high priority.
		/// </summary>
		High = 3,

		/// <summary>
		/// The highest priority.
		/// </summary>
		Highest = 4
	}
}
